package kr.co.buskers.repository.domain;

public class MemberGenre {

	private int memberGenreNo;
	private int memberNo;
	private int genreNo;
	
	public int getMemberGenreNo() {
		return memberGenreNo;
	}
	public void setMemberGenreNo(int memberGenreNo) {
		this.memberGenreNo = memberGenreNo;
	}
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	public int getGenreNo() {
		return genreNo;
	}
	public void setGenreNo(int genreNo) {
		this.genreNo = genreNo;
	}
	
	
}
